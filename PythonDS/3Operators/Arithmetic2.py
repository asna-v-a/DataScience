# multiplication of 3 numbers

# Take user input and convert to integer using int() function
num1 = int(input("Enter the first number: "))  # The input will be read as an integer
num2 = int(input("Enter the second number: "))  # The input will be read as an integer
num3 = int(input("Enter the third number: "))  # The input will be read as an integer

# Multiply the three numbers
result = num1 * num2 * num3  # Multiplying all three numbers

# Print the result in a readable format
print(num1, "*", num2, "*", num3, "=", result)  # Displays the multiplication result
