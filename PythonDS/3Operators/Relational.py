# Relational operators - used for comparison
# >  <  >=  <=  ==  !=   - output will be True or False

num1 = 10
num2 = 20

print(num1 > num2)
print(num1 < num2)
print(num1 >= num2)
print(num1 <= num2)
print(num1 == num2)
print(num1 != num2)

print(True < False)# Ture -> 1 and False -> 0
print(True > False)